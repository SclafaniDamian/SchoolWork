#include<average_functions.h>

// Function that will compute the average of three doubles
double Average(double x, double y, double z) {
  return (x + y + z) / 3;
}

// funciton to compute av of two characters
char Average(char c1, char c2) {
  return (c1 + c2) / 2;
}